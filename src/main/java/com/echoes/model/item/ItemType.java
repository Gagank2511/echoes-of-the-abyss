package com.echoes.model.item;

/**
 * Enumeration of item types available in the game.
 *
 * <p>This categorises items based on their usage and behaviour within the game world.</p>
 */
public enum ItemType {
    /** Items that can be consumed to provide temporary effects. */
    CONSUMABLE,
    /** Items that provide permanent stat boosts. */
    STAT_BOOST
}