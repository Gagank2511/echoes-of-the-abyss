package com.echoes.model.character.enemy;

/**
 * Enumeration of enemy types available in the dungeon.
 *
 * <p>Each type represents a different kind of enemy with varying stats,
 * abilities, and difficulty levels.</p>
 */
public enum EnemyType {

    /** A small, weak goblin enemy. */
    GOBLIN,
    /** An undead skeleton warrior. */
    SKELETON,
    /** A large, brutish troll. */
    TROLL,
    /** A young dragon with fire breath. */
    DRAGON_WHELP,
    /** A massive stone construct. */
    STONE_GOLEM,
    /** A powerful undead sorcerer. */
    LICH,
    /** The ultimate boss, a massive abyss dragon. */
    ABYSS_DRAGON
}